package com.vti.entity;

public class MyMath {
	public int sum(int a, int b) {
		return a + b;
	}

	public Float sum(float a, float b) {
		return a + b;
	}

	public byte sum(byte a, byte b) {
		return (byte) (a + b);
	}

	public Float sum(byte a, float b) {
		return (a + b);
	}

	public Float sum(float a, int b) {
		return (a + b);
	}

	public int sum(byte a, int b) {
		return (a + b);

	}

}
